export function titleTranslate(title) {
  switch (title) {
    case 'boxes':
      return 'КронБоксы'
    case 'posters':
      return 'Постеры'
    case 'mugs':
      return 'Кружки'
    case 'merch':
      return 'Мерч'
    case 'chancellery':
      return 'Канцелярия'
    case 'accessories':
      return 'Аксессуары'
    default:
      return 'Товары'
  }
}
